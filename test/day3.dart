import 'dart:convert';
import 'dart:math' as math;

import 'package:aoc_2020/fields.dart';
import 'package:test/test.dart';

enum TreeFieldCell { empty, tree }

class TreeField {
  final Map<Position, TreeFieldCell> cells;
  final int width;
  final int height;

  TreeField(this.height, this.width, this.cells);

  TreeFieldCell operator [](Position position) {
    position = Position(position.x % width, position.y);
    return cells[position] ?? TreeFieldCell.empty;
  }

  int slide(Offset offset) {
    var position = Position(0, 0);
    var result = 0;
    while (position.y < height) {
      if (this[position] == TreeFieldCell.tree) {
        result++;
      }
      position += offset;
    }
    return result;
  }

  static TreeField parse(String text) {
    var result = <Position, TreeFieldCell>{};
    var lineNo = -1;
    var maxWidth = 0;
    for (var line in LineSplitter.split(text).map((line) => line.trim())) {
      lineNo++;
      maxWidth = math.max(maxWidth, line.length);
      for (var colNo = 0; colNo < line.length; colNo++) {
        if (line[colNo] == '#') {
          result[Position(colNo, lineNo)] = TreeFieldCell.tree;
        }
      }
    }
    return TreeField(lineNo + 1, maxWidth, result);
  }
}

int part1(String input, Offset direction) =>
    TreeField.parse(input).slide(direction);

int part2(String input, Iterable<Offset> directions) {
  var field = TreeField.parse(input);
  return directions.map(field.slide).fold(1, (r, e) => r * e);
}

void main() {
  test('sample', () {
    var input = '''..##.......
#...#...#..
.#....#..#.
..#.#...#.#
.#...##..#.
..#.##.....
.#.#.#....#
.#........#
#.##...#...
#...##....#
.#..#...#.#''';
    expect(part1(input, Offset(3, 1)), 7);
    expect(
        part2(input, [
          Offset(1, 1),
          Offset(3, 1),
          Offset(5, 1),
          Offset(7, 1),
          Offset(1, 2)
        ]),
        336);
  });

  test('part1', () {
    print(part1(input, Offset(3, 1)));
  });

  test('part2', () {
    print(part2(input, [
      Offset(1, 1),
      Offset(3, 1),
      Offset(5, 1),
      Offset(7, 1),
      Offset(1, 2)
    ]));
  });
}

